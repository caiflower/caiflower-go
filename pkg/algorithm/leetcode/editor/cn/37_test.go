func solveSudoku(board [][]byte) {
	var occupyRow, occupyCol [10][9]bool
	var occupyBlock [10][3][3]bool

	// åˆå§‹åŒ–å ç”¨æƒ…å†µ
	for i, l := range board {
		for j, c := range l {
			if c != '.' {
				v := c - '0'
				occupyRow[v][i] = true
				occupyCol[v][j] = true
				occupyBlock[v][i/3][j/3] = true
			}
		}
	}

	var dfs func(i, j int) bool

	dfs = func(i, j int) bool {
		if i >= 9 {
			return true
		}

		j1 := j + 1
		i1 := i
		if j1 >= 9 {
			j1 = 0
			i1 += 1
		}

		if board[i][j] == '.' {
			for n := 1; n <= 9; n++ {
				if occupyRow[n][i] || occupyCol[n][j] || occupyBlock[n][i/3][j/3] {
					continue
				}
				occupyRow[n][i] = true
				occupyCol[n][j] = true
				occupyBlock[n][i/3][j/3] = true
				board[i][j] = '0' + byte(n)

				if dfs(i1, j1) {
					return true
				}

				occupyRow[n][i] = false
				occupyCol[n][j] = false
				occupyBlock[n][i/3][j/3] = false
				board[i][j] = '.'
			}
			return false
		} else {
			return dfs(i1, j1)
		}
	}

	dfs(0, 0)

	return
}

