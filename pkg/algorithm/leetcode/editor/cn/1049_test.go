func lastStoneWeightII(stones []int) int {
	sum := 0
	for _, v := range stones {
		sum += v
	}
	half := sum / 2
	dp := make([]int, half+1)

	for _, v := range stones {
		for j := half; j >= v; j-- {
			dp[j] = max(dp[j], dp[j-v]+v)
		}
	}

	return abs1((sum - dp[half]) - dp[half])
}

func abs1(x int) int {
	if x < 0 {
		return -x
	}
	return x
}

